package com.learning.java_basics;

import java.util.Scanner;

public class RelationalOperators {
public static void main(String[] args) {
	Scanner scan = new Scanner(System.in);
	
	int num1 =1;
	int num2 = 2;
	
	
	System.out.println("num1 > num2 is " + (num1 > num2));
	System.out.println("num1 < num2 is " + (num1 < num2));
	System.out.println("num1 >= num2 is " + (num1 >= num2));
	System.out.println("num1 <= num2 is " + (num1 <= num2));
	System.out.println("num1 == num2 is " + (num1 == num2));
	System.out.println("num1 != num2 is " + (num1 != num2));
}
}
